                    <h2> Oportunidad para realizar la solicitud de retracto. </h2>

                    <p>
                        La oportunidad para ejercer el derecho a retractarse de la venta a distancia se tendrá que dar dentro de los cinco (5) días siguientes a la recepción del producto que se envíe a domicilio hasta el sitio indicado por el cliente que haya adquirido sus productos mediante los portales.
                        <br>
                        Después del transcurso de los cinco (5) días anteriormente señalados, se entiende que el cliente recibe a satisfacción el producto y no tendrá la oportunidad de realizar cualquier petición frente al mismo.

                    </p>

                    <h2> Procedimiento para solicitar el retracto. </h2>

                    <p>
                        El procedimiento para efectos de solicitar de los productos pagados con dineto en efectivo es la siguiente:

                        <ol>
                            <li>
                                Una vez recibido el producto, el cliente dentro de los cinco (05) días siguientes tendrá la oportunidad de comunicarse con DROGUERIA MINIFARMA S.A.S. para informar su deseo de hacer el cambio mediante los siguientes medios:

                                <ol type="a">
                                    <li>
                                       Al correo electrónico  <a href="mailto:info@drazamed.com">info@drazamed.com</a>
                                    </li>
                                    <li>
                                        Dejando un mensaje en el apartado de PQR´S de cualquiera de los portales.
                                    </li>
                                </ol>
                            </li>
                            <li>
                                Para efectos de realizar la reclamación se requiere en principio allegar por cualquier medio a DROGUERIA MINIFARMA S.A.S. la factura de venta, para efectos de identificar, fechas, características del producto, precios y demás información importante de los productos objeto de la compraventa.
                            </li>
                            <li>
                                Una vez recibida la solicitud de retracto DROGUERIA MINIFARMA S.A.S. deberá responder dentro de los cinco (5) días siguientes a la recepción de la misma.
                            </li>
                            <li>
                                Si la respuesta es negativa, lo informará presentando los argumentos que sustentan a la misma.
                            </li>
                            <li>
                                Si la respuesta es positiva, DROGUERIA MINIFARMA S.A.S. comunicará al cliente dicha decisión. En este caso el consumidor deberá devolver el producto a DROGUERIA MINIFARMA S.A.S. por el mismo medio en que lo recibió y deberá estar en las mismas condiciones en que lo recibió.
                            </li>
                            <li>
                                Si el cliente lo comunica de manera expresa en su solicitud, podrá solicitar que sea recogido en el lugar en donde recibió el producto. El cliente en este caso deberá asumir el costo del transporte para la recolección del producto, el cual será informado en la respuesta afirmativa descrita en el numeral inmediatamente anterior. Con la aceptación de la recolección por parte de DROGUERIA MINIFARMA S.A.S, el cliente acepta que este costo sea compensado disminuyéndolo de la suma sujeta a devolución.
                            </li>
                            <li>
                                A pesar de que la respuesta de DROGUERIA MINIFARMA S.A.S. sea positiva, el domiciliario podrá revisar el producto y si ve cualquier desperfecto señalado en la presente política, podrá negarse a realizar la respectiva devolución del dinero o cambio según corresponda.
                            </li>
                            <li>
                                Una vez aceptado el producto por parte del domiciliario y hecha la devolución del dinero, el cliente deberá firmar una copia simple de la factura en la que acepta que se le devolvió la suma a la que asciende la devolución o que recibió el nuevo producto, y que declara a paz y salvo por todo concepto a DROGUERIAS MINIFARMA S.A.S.
                            </li>

                        </ol>
                    </p>

                    <h2> Reversión del Pago realizado mediante instrumentos de carácter electrónico.</h2>

                    <p>
                        Cuando el cliente haya realizado el pago de la venta por intermedio de una tarjeta de crédito, débito o cualquier otro instrumento de pago electrónico, los clientes podrán solicitar la reversión del pago en los siguientes casos que se enumeran a continuación:

                        <ol>
                            <li>
                                Cuando el pago corresponda a una compra no solicitada por el cliente.
                            </li>
                            <li>
                                Cuando la venta del producto sea resultado u objeto de un fraude.
                            </li>
                            <li>
                                Cuando el cliente no haya recibido el producto dentro del tiempo estimado para ello.
                            </li>
                            <li>
                                Cuando el producto que se entrega no corresponda al que se solicitó
                            </li>
                            <li>
                                Cuando el producto sea defectuoso.
                            </li>
                        </ol>


                      Para la reversión de este pago se deberá proceder de la siguiente forma:

                      <ol>
                            <li>
                                Dentro de los cinco (5) días siguientes a los que el cliente tuvo conocimiento de la transacción fraudulenta o no solicitada o después del plazo en que debió haber recibido el producto o desde cuando lo recibió defectuoso o sin que correspondiera a lo solicitado, deberá presentar queja en este sentido, por cualquiera de los medios que se relacionan a continuación:

                                <ol type="a">
                                    <li>
                                       Al correo electrónico  <a href="mailto:info@drazamed.com">info@drazamed.com</a>
                                    </li>
                                    <li>
                                        Dejando un mensaje en el apartado de PQR´S de cualquiera de los portales.
                                    </li>
                                </ol>
                            </li>
                            <li>
                                La queja deberá contener como mínimo, con la siguiente información

                                <ol type="a">
                                    <li>
                                        Manifestación expresa de las razones que fundamentan la solicitud de reversión del pago.
                                    </li>
                                    <li>
                                        Indicación de la causal que sustenta la petición,
                                    </li>
                                    <li>
                                        Valor por el que se solicita la reversión.
                                    </li>
                                    <li>
                                        Identificación de la cuenta bancaria, tarjeta de crédito o instrumento de pago al que fue cargada la operación.
                                    </li>
                                </ol>
                            </li>
                            <li>
                                DROGUERIA MINIFARMA S.A.S emitirá constancia de la presentación de esta, con indicación de la fecha y causal que la sustentan.
                            </li>
