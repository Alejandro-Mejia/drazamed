                            <li>
                                Que los productos no correspondan a los lotes que se encontraban en poder del expendedor.
                            </li>
                            <li>
                                Los productos que se encuentren vencidos, salvo que la fecha de vencimiento se hubiese causado antes de la fecha de la compra.
                            </li>
                            <li>
                                Productos cosméticos.
                            </li>
                            <li>
                                Medicamentos que por sus características requieran condiciones de refrigeración
                            </li>
                            <li>
                                Medicamentos de control especial vendidos bajo formula médica.
                            </li>
                        </ol>

